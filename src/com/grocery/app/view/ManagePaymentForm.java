    private void enterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterBtnActionPerformed
        try {
            String oid=oidTxt.getText();
            String custID=custIDTxt.getText();
            this.oid=oid;
            this.custID=custID;
            Order order = orderController.searchOrder(oid);
            if(order!=null){
                custIDTxt.setText(order.getCustomerId());
                custNameTXt.setText(customerController.searchCustomer(order.getCustomerId()).getName());
                ArrayList<OrderDetail> searchOrderDetail = orderDetailController.searchOrderDetail(oid);
                double totAmount=0;
                for (OrderDetail searchOrderDetail1 : searchOrderDetail) {
                    totAmount+=searchOrderDetail1.getUnitPrice()*searchOrderDetail1.getQty();
                }
                subTotTxt.setText(Double.toString(totAmount));

            }else{
                JOptionPane.showMessageDialog(this, "Order not found !");
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_enterBtnActionPerformed

