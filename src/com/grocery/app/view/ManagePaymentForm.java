    private void enterBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterBtn1ActionPerformed
        try {
            String oid=oidTxt1.getText();
            String custID=custIDTxt1.getText();
            this.oid=oid;
            this.custID=custID;
            final String payId = paymentIDTxt1.getText();
            Payment payment = paymentController.searchPayment(payId);
            if (payment!=null) {
                Order order = orderController.searchOrder(payment.getOid());
            if(order!=null){
                custIDTxt1.setText(order.getCustomerId());
                custNameTXt1.setText(customerController.searchCustomer(order.getCustomerId()).getName());
                oidTxt1.setText(order.getId());
                double subTotal = payment.getTotal();
                double discount = payment.getDiscount();
                double total = subTotal-discount;
                totalTxt1.setText(Double.toString(total));
                double cash = payment.getCash();
                balanceTxt1.setText(Double.toString(cash-total));
                subTotTxt1.setText(Double.toString(subTotal));
                discountTxt1.setText(Double.toString(discount));
                cashTxt1.setText(Double.toString(cash));
            }else{
                JOptionPane.showMessageDialog(this, "Order not found !");
            }
            }else{
                JOptionPane.showMessageDialog(this, "Payment Details not found !");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_enterBtn1ActionPerformed

