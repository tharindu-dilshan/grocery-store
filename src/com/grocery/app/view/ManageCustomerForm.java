    private void custAddBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_custAddBtActionPerformed
        try {
            boolean isAdded = custController.addCustomer(
                    new Customer(custIDAddTxt.getText(),
                            custNameAddTxt.getText(),
                            custAddressAddTxt.getText(),
                            Long.parseLong(custTelephoneAddTxt.getText())));
            if(isAdded){
                JOptionPane.showMessageDialog(this, "Customer Added Successfully !!");
                custAddClearBtn.doClick();
                custIDAddTxt.requestFocus();
            }else {
                JOptionPane.showMessageDialog(this, "Customer Added Falier !!");
                custIDAddTxt.requestFocus();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_custAddBtActionPerformed
    
