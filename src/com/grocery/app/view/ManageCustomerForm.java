    private void custIDDeleteTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_custIDDeleteTxtActionPerformed
        String custID=custIDDeleteTxt.getText();
        try {
            this.custID=custID;
            Customer cust = custController.searchCustomer(custID);
            if(cust!=null){
                custIDDeleteTxt.setText(cust.getId());
                custNameDeleteTxt.setText(cust.getName());
                custAddressDeleteTxt.setText(cust.getAddress());
                custTelephoneDeleteTxt.setText("0"+Long.toString(cust.getTelephone()));
            }else{
                JOptionPane.showMessageDialog(this, "No Customer found..");
                custNameDeleteTxt.setText("");
                custAddressDeleteTxt.setText("");
                custTelephoneDeleteTxt.setText("");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ThogaKadeMW.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_custIDDeleteTxtActionPerformed
    
