    private void itemICodeDeleteTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemICodeDeleteTxtActionPerformed
        try {
            String itemCode=itemICodeDeleteTxt.getText();
            this.itemCode=itemCode;
            Item item = itemController.searchItem(itemCode);
            if(item!=null){
                itemICodeDeleteTxt.setText(item.getCode());
                itemDescDeleteTxt.setText(item.getDescription());
                itemQTYDeleteTxt.setText(Integer.toString(item.getQty()));
                itemUPDeleteTxt.setText(Double.toString(item.getUnitPrice()));
            }else{
                JOptionPane.showMessageDialog(this, "No Item Found..");
                itemDescDeleteTxt.setText("");
                itemQTYDeleteTxt.setText("");
                itemUPDeleteTxt.setText("");
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ThogaKadeMW.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_itemICodeDeleteTxtActionPerformed
    
    private void itemDeleteBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemDeleteBtActionPerformed
        try {
            boolean isDeleted = itemController.deleteItem(itemICodeDeleteTxt.getText());
            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Item Deleted Successfully !!");
                itemDeleteClearBtn.doClick();
                itemICodeDeleteTxt.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Item Delete Fail !!");
                itemICodeDeleteTxt.requestFocus();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ThogaKadeMW.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_itemDeleteBtActionPerformed
    
