    private void itemAddBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemAddBtActionPerformed
        try {
            boolean isAdded = itemController.addItem(new Item(itemICodeAddTxt.getText(), itemDescAddTxt.getText(), Integer.parseInt(itemQTYAddTxt.getText()), Double.parseDouble(itemUPAddTxt.getText())));
            if(isAdded){
                JOptionPane.showMessageDialog(this, "Item Added Successfully !!");
                itemAddClearBtn.doClick();
                itemICodeAddTxt.requestFocus();
            }else {
                JOptionPane.showMessageDialog(this, "Item Added Falier !!");
                itemICodeAddTxt.requestFocus();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_itemAddBtActionPerformed
    
